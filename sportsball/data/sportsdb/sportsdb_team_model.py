@MEMORY.cache(ignore=["session"])
def _cached_create_sportsdb_team_model(
    team_id: str,
    name: str,
    points: float,
    session: requests_cache.CachedSession,
    dt: datetime.datetime,
    league: League,
    version: str,
) -> TeamModel:
    return _create_sportsdb_team_model(
        team_id=team_id,
        name=name,
        points=points,
        session=session,
        dt=dt,
        league=league,
        version=version,
    )


def create_sportsdb_team_model(
    team_id: str,
    name: str,
    points: float,
    session: requests_cache.CachedSession,
    dt: datetime.datetime,
    league: League,
) -> TeamModel:
    """Create a team model based off the sportsdb NFL response."""
    if not pytest_is_running.is_running() and dt < datetime.datetime.now().replace(
        tzinfo=dt.tzinfo
    ) - datetime.timedelta(days=7):
        return _cached_create_sportsdb_team_model(
            team_id=team_id,
            name=name,
            points=points,
            session=session,
            dt=dt,
            league=league,
            version=VERSION,
        )
    with session.cache_disabled():
        return _create_sportsdb_team_model(
            team_id=team_id,
            name=name,
            points=points,
            session=session,
            dt=dt,
            league=league,
            version=VERSION,
        )
